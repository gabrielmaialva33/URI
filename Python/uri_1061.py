null = input().split()
di = int(null[1])
hi, mi, si = map(int, input().split(':'))
null = input().split()
df = int(null[1])
hf, mf, sf = map(int, input().split(':'))
t = abs(((di * 86400) + (hi * 3600) + (mi * 60) + si) - ((df * 86400) + (hf * 3600) + (mf * 60) + sf))
print('{} dia(s)'.format(t // 86400))
print('{} hora(s)'.format((t % 86400) // 3600))
print('{} minuto(s)'.format((((t % 86400) % 3600) // 60)))
print('{} segundo(s)'.format((((t % 86400) % 3600) % 60)))